/* --------------------------------------------------------------------------- */
$(document).on('ready',function()
{
	$("#btn_menu").click(toggleMainMenu);
});
/* --------------------------------------------------------------------------- */
function toggleMainMenu() 
{
	var menu = $("#main_menu");

	if(menu.hasClass("active"))
		menu.removeClass("active");
	else
		menu.addClass("active");
}
